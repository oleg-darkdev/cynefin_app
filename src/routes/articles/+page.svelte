    <h1 class="h1-blogtitle">Three helpful stats for shortstaffed restaurants</h1>
    <div class="tag_list _40b">
      <a id="w-node-_5f569c67-6ca0-374e-f5a3-b0bfde9b9d3d-7fdb6143" href="/tag/restuarants" class="button-tag small w-inline-block">
